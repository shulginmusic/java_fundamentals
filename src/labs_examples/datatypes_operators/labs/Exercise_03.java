package labs_examples.datatypes_operators.labs;

/**
 * Please demonstrate the use of all arithmetic operators below. These include:
 *
 * addition, subtraction, multiplication, division and modulus
 *
 */
class ArithmeticOperators {

    public static void main(String[] args) {

        // write your code below
        int ten = 10;
        int five = 5;

        System.out.println(ten + five); //15
        System.out.println(ten - five); //5
        System.out.println(ten * five); //50
        System.out.println(ten / five); //2
        System.out.println(ten % five); //0
        double x = Math.pow(4.0, 3.0);
        System.out.println(x);
    }
}
